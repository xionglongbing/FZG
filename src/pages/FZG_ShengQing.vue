          1.南昌市
        </div>
        <div style="font-size: 15px; text-align: left; text-indent: 25px">
          南昌市，历史上称豫章、洪都、灌城等，南昌现为江西省省会，为全省政治、经济、文化、交通中心。位于赣水之滨、鄱湖之尾，是一座具有悠久历史和灿烂文化的江南古城，又是一座具有光荣革命传统的英雄城市。它负江临湖，翠峰环绕，东南面向著名的武夷山脉，西南直通闻名于世的井冈山，北邻游览胜地人文圣山——庐山，千里赣江纵贯全境，是中原和南方闽越之间的黄金水道。
        </div>
        <div style="font-size: 15px; text-align: left; text-indent: 25px">
          南昌是一座历尽沧桑的古城，早在两千多年前的西汉时，这里已是“江南昌盛之地”，故名“南昌”。自汉初设豫章郡以来，历经两千多年，一直是豫章、洪州、南昌等郡、州、府的治所。由于土地肥沃，物产丰富，经济繁荣，五业兴旺，逐渐成为东南著名的都会。1986年12月，南昌市荣膺国务院公布的第二批国家历史文化名城，以“保存文物特别丰富，具有重大历史价值和革命意义”而为世人瞩目。全市下辖东湖、西湖、湾里、青山湖、青云谱五区，并管辖南昌、新建、进贤、安义四县。全市区域面积约7402平方千米，2010年总人口约504万。
        </div>
        <div style="font-size: 15px; text-align: left; text-indent: 25px">
          南昌境内地势平坦，平均海拔25米左右。位于鄱阳湖平原中部，土地肥沃，气候湿润。城区以北是赣江注入鄱阳湖的尾闾地带，江流分岔，港汊密布。西北部是绵延近百里的西山，恰似一道屏障崛起于坦荡的平原之上，气势格外壮观。从西山麓到赣江边，红壤丘陵连绵起伏，冈阜浅丘翻腾似浪，适宜于栽种油茶和果木。东郊是素有鱼米之乡之称的赣抚平原，已成为粮食、蔬菜生产的重要基地。南昌有东湖、西湖、南湖、北湖、青山湖、艾溪湖、碟子湖、象湖、军山湖、瑶湖等数百个大小湖泊，湖在城中，城在湖中。由于常年泥沙冲积，形成了不少洲地，包括官洲、潮王洲（黄牛洲）、打搅洲、扬子洲（羊子洲）、大洲和蓼子洲等。
        </div>
        <div style="font-size: 15px; text-align: left; text-indent: 25px">
          在南昌城西北方向，有一条自西向东经安义县城注入鄱阳湖的河流——潦河，它就是最早养育南昌先民的水域，是南昌地区人类活动的历史见证。远在五千多年前的新石器时代，这里已开始出现原始村落。在三四千年前的夏、商、周时代，南昌地区属“扬州之域”，从30多处商周遗址的发现及其出土文物看，市内南起青云谱、北至艾溪湖这一弧形地带是居民的聚集地。春秋战国时期，南昌地区属楚之东境、吴之西境，与中原文化日趋融和，逐渐向封建社会过渡。秦始皇统一中国后，将楚境分为三郡，南昌地属九江郡，此时这里已逐步形成较发达的农业、手工业和商业，具备了建城的条件。
        </div>
         <div style="font-size: 15px; text-align: left; text-indent: 25px">
           建国以前，南昌一直是以政治、商业为主的消费城市，全国解放后，特别是改革开放以来，英雄城的面貌发生了巨大的变化。随着国民经济的发展，全市逐步形成了飞机制造、汽车制造、冶金、机电、纺织、化工、医药等现代化工业体系，以电子信息、生物工程、新材料等为代表的新兴高新技术产业也具有一定的水平。随着京九线的开通，南昌“东驰西进、南来北往”的区位优势更为突出，位于南昌大桥东端的占地1,700余亩的洪城大市场，拥有纺织、服装、食品、日杂等几大门类近万种商品，是全国九大日用品中心批发市场之一。
        </div>
         <div style="font-size: 15px; text-align: left; text-indent: 25px">
           建国以来，南昌市交通运输事业发展很快，有浙赣、赣宁、赣湘、南浔、京九几条铁路干线，是京九线上惟一的省会城市。占地6,800亩、投资11亿元的民用机场——昌北机场已于1999年9月顺利通航，拥有宽敞明亮的候机大厅，4D级的机场跑道，精密先进的导航系统等。这座现代化的民用航空港的通航，为江西省民航事业发展奠定了扎实基础。昌九高速公路、南莲高速公路的开通，对加快南昌及附近地区的经济发展和改革开放有重要意义。昌北合资牛行水厂的兴建，使生产、生活用水更为方便。与交通运输相配套的邮电通讯事业发展迅猛，是江西省的邮电通讯枢纽中心。
        </div>
