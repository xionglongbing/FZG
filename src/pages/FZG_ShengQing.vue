        <div style="font-size: 20px; font-weight: bolder">文学</div>
<div style="font-size: 15px; text-align: left; text-indent: 25px">
  在古代，江西文学有过光辉灿烂的发展史，尽管起步不早，但发展速度很快。东晋以前，江西文人少有作品传世，直到陶渊明的出现。东晋后期，江西文坛上异峰突起般地出现了一位全国杰出的伟大诗人陶渊明，他不仅是江西文学史上第一位伟大的文学家，而且有中国田园诗派的开山祖之誉。著有《陶渊明集》，今存诗l20余首，文九篇，辞赋三篇。相传志怪小说集《搜神后记》10卷也是他的作品。南朝宋时，南昌雷次宗，著有文集20卷。
        </div>
        <div style="font-size: 15px; text-align: left; text-indent: 25px">
          隋唐五代时期，江西作家作品明显增多，逐步形成了一个诗人群体。据《全唐诗》、《全唐文》及有关史料统计，唐五代文学家总数共计约3770余家，其中江西有93家，占总数的2.46%。这个诗人群体，除部分分别为南昌、九江、高安、奉新、上饶、鄱阳和广丰人外，几乎都集中于袁州的宜春，尤其是晚唐五代诗人更是如此。这时期的代表作家有宜春的郑谷，九江的舒元舆和南昌的王定保。郑谷有诗集《云台编》三卷传世，今人整编为《郑谷诗集》，存诗320多首。舒元舆散文有较高成就，注重内容，讲究形象化，不以议论见长。《全唐文》收其散文一卷l6篇。王定保是五代笔记小说家，撰有《唐摭言》15卷，多收入《太平广记》中。尤其可喜的是女诗人开始登上文坛，主要代表诗人有鄱阳的程长文和宜春的彭伉妻张氏，《全唐诗》收有她们的作品。
        </div>
        <div style="font-size: 15px; text-align: left; text-indent: 25px">
          两宋是江西古代文学发展的高峰期，是江西古代文学的黄金时代。全国许多著名的大文学家、大诗人、大词人、小说家和散文家，都先后出现在这时期的江西文坛上。如晏殊、欧阳修、曾巩、王安石、晏几道、黄庭坚、洪迈、杨万里、姜夔、文天祥等。他们是这时期江西文坛最卓越的代表作家。这时期形成以黄庭坚为首的举世瞩目的江西诗派，其流传之久，影响之大，超过中国文学史上的任何诗派。词虽起步晚，但发展速度很快，而且达到了空前绝后的艺术高峰。小说和笔记也空前繁荣，各种内容和形式的笔记层出不穷，而且出现了全国篇幅最长、内容最广、规模最大的笔记小说集《夷坚志》和笔记集《容斋随笔》。散文的最高成就也出现在这一时期。唐宋古文运动中的八大家，宋代占六家，江西有欧阳修、曾巩、王安石。据《全宋诗》(北宋都分)统计共有诗人3160余家，其中江西占236家，占全国的7.47%。在《全宋词》中，江西占的比例更高。据统计《全宋词》共收词人l330余家，江西就占178家，占全国的l3.38%，仅次于浙江而居全国第二位。在八卷本的《中国文学大辞典》中，共收宋代文学家940余人，江西就有186人，占全国的19.74%。在这一时期，江西作家队伍出现了众多的以血缘关系而形成一家几代的作家网。如宜黄的乐史、乐黄目父子，临川的晏殊、晏几道父子，南丰的曾巩、曾布、曾肇、曾纡、曾纮、曾惇、曾协等祖孙三代，新余的刘敞、刘攽兄弟和孔文仲、孔武仲、孔平仲三孔，临川的王益、王安石、王安国、王安礼、王雱等三代五王，修水的黄庶、黄大临、黄庭坚两代三黄，南昌的洪朋、洪刍、洪炎、洪羽四兄弟，鄱阳的洪皓、洪适、洪遵、洪迈两代四洪等等。
        </div>
        <div style="font-size: 15px; text-align: left; text-indent: 25px">
          元明是江西文学处于极其复杂的时期。就基本情况而言，元曲，尤其是戏剧文学获得了空前的发展和繁荣，成为这时期最高成就的代表，而其它方面成就明显不如宋代，处于下降时期。就诗来说，是相持偏低时期；就词来说，是衰落时期；就小说与笔记来说，也是下降时期；就散文而言，也远不如宋代。《四库全书》收入这时期江西作家作品共70人，75部，比宋代的87人，ll6部少得多。八卷本《中国文学大辞典》收元明文学家l290人，其中江西省l35人，占全国的l0.4%。比宋代的942人，江西省186人，占全国的19.74%差得很远。就作家队伍及其组成情况而言，这时期江西最著名的文学家是汤显祖。这时期的江西作家队伍之庞大却是史无前例。在元代，较有成就的散曲作家主要有刘时中、周德清、赵善夫、汪元亨等；诗歌作家主要有虞集、范梈、揭傒斯、熊朋来、吴澄等；词人主要的有燕公楠、曾允元、刘洗、程文海等。进入明代以后，江西作家队伍不仅远远超过元代，也远远超过宋代的规模，呈现着空前壮大的趋势。除汤显祖外，较有名的诗人有危素、刘崧、张羽、杨士奇、解缙、刘炳、梁兰、胡俨、李时勉、胡直、郭子章、严嵩、万时华、刘淑英等；较有名的词人有梁寅、聂大年、夏言、曾灿等；较有名的小说及笔记作家有李祯、邓志谟、刘元卿等；较有名的散文家有陈谟、周是修、梁潜、王直、何乔新、吴与弼等。
        </div>
        <div style="font-size: 15px; text-align: left; text-indent: 25px">
清代(1840年以前)和近代是江西古代文学的衰落时期。无论哪一种文学样式的作品都无法与以前发达的时代相比，尤其小说创作远远落后于全国，几乎没有一部小说能列入全国先进行列。清代江西的代表作家是蒋士铨。无论戏曲、诗、词、文都有很高成就。近代江西代表作家是诗人陈三立和词人文廷式。尽管从纵向比较来看处于衰落时期，但清代江西文学还是有一定成就的，主要体现在散文和诗词方面。较有名的散文家有王猷定、魏禧、彭士望、蒋士铨、郑日奎、帅我、帅仍祖、乐钧等；词和全国一样，处于第二高峰期，主要词人有蒋士铨、乐钧，其次有李元鼎、熊文举、余鼎、陈允衡、符兆纶、吴嵩梁、舒梦兰等；主要诗人除上述词人外还有曾畹、李绂、李来泰、谢启昆等；戏剧作家主要是蒋士铨；小说成就不很高，主要是短篇较好，如乐钧的文言小说集和徐芳、陈宏绪的笔记小说。进入鸦片战争以来的近代，陈三立和文廷式是江西文坛诗词最高成就的代表作家。
        </div>
        <div style="font-size: 15px; text-align: left; text-indent: 25px">
从“五四”运动到新中国成立的三十年中，江西现代文学发展较为缓慢，作家队伍较小，作品数量也较少，成就高的作家作品不多，且多为江西籍而在省外生活与工作，在江西本地生活的作家更少。这时期的主要作家有邹韬奋、方志敏、王礼锡、罗黑芷、曾今可、熊佛西、夏征农、白采、胡先骕、饶孟侃、彭芳草等，主要文学研究家有陈寅恪、游国恩、萧涤非、龙榆生、王易、李平心等，还有文艺理论家彭康。
        </div>
         <div style="font-size: 15px; text-align: left; text-indent: 25px">
建国后，到“文化大革命”前的l7年中，江西文学的发展也不快。这时期无论文学的样式与作品的数量都不多，作家队伍较小，而且分散。有些名作家、名诗人都分散在省外各地生活。如杜宣、吴运铎、曾秀苍、公刘、彭荆风、周良沛、天蓝等。新一代作家尚在成长之中。当时在江西本地生活的作家加入中国作协的只有几人。“文化大革命”十年中，江西文学创作与研究，除少数几个作家有一定发展外，绝大多数处于停笔或被迫停笔状态。无论作家队伍和作品数量都处于低潮。改革开放以来，江西文学的各种样式都得到了充分而全面的发展，尤其是小说的发展，其中突出的又是中长篇小说的史无前例的大发展；其次是报告文学、纪实文学和传记文学的发展；第三是诗歌的发展；第四是儿童文学的发展；散文也有较大的发展；还有一点不可忽视的是一向不发达的江西文学理论、文学评论和文学研究，尤其是古典文学研究均有了长足的发展和进步。此外，外国文学翻译与研究也成正比地向前发展。与此相适应的是民间文学的整理与研究、文学期刊的编辑与出版都有了明显的发展。
        </div>
