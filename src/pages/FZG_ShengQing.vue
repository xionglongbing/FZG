          2.景德镇市
        </div>
        <div style="font-size: 15px; text-align: left; text-indent: 25px">
          富饶美丽的赣东北大地上，镶嵌着一颗历千年而不衰的闪光明珠景德镇。这座以悠久制瓷历史和精湛制瓷技艺而著称于世的城市，其千年不灭的窑火，烧制了无数精美绝伦的陶瓷制品，也孕育了多彩绚烂的陶瓷文化。明清时期景德镇即成为与朱仙镇、汉口镇、佛山镇齐名的四大工商名镇之一，20世纪80年代初，又名列全国首批历史文化名城之中。在24座名城中，它是惟一以工业的专门化而著名的历史文化名城，向有“瓷都”之美名。
        </div>
        <div style="font-size: 15px; text-align: left; text-indent: 25px">
          景德镇地处江西省的东北部，位于黄山、怀玉山余脉与鄱阳湖平原的过渡地带，居浙、皖、赣三省交界处。东接婺源、德兴，南连万年、弋阳，西邻鄱阳，北接安徽的祁门、东至，全镇的形状颇似一朵含苞欲放的百合花。全市面积约5256平方千米，2010年全市人口约158万，境内峰峦环峙，江河绕流，风景优美。市内东、西、北三面多低山，东南部丘陵起伏，是一处群山环抱的丘陵盆地，主要山峰有东部的白石塔和五花尖等，海拔皆在1000米以上。中部和西南部为昌江谷地，是鄱阳湖的一部分，海拔在50至100米之间。起源于安徽祁门县境内的昌江，自西南经皖赣边界，由北向南流经景德镇市区。
        </div>
        <div style="font-size: 15px; text-align: left; text-indent: 25px">
          景德镇是一座历史悠久的古城，春秋战国时期属吴楚两国拉锯地区，故有“吴头楚尾”之称。秦时属九江郡，汉属豫章郡鄱阳县，三国时统为吴地，属扬州。景德镇原是浮梁县辖区，该县地处昌江南岸，故始名昌南镇，后因东晋陶侃“擒江东寇”于此，遂更名为新平镇。唐武德四年（公元621年）置新平县，开元四年（公元716年），改名新昌县，天宝元年（公元742年）更名浮梁县。北宋景德元年（公元1004年），因镇产青白瓷质地优良，遂以皇帝年号为名置景德镇。景德镇自始名之日起，一直是浮梁县辖镇。元代升为浮梁州，属饶州路，明初为浮梁县，属饶州府。1953年，景德镇升为省辖市，现景德镇市辖一市一县两区，即乐平市、浮梁县、珠山区和昌江区。
        </div>
        <div style="font-size: 15px; text-align: left; text-indent: 25px">
          景德镇的许多古迹，都与陶瓷业的发展繁荣紧密联系在一起。千余年经久不衰的陶瓷生产为景德镇留下了许多古代窑址和大量的古瓷片、古窑具堆积层。离市区不远的湖田古窑，它兴烧于五代，历经宋、元至明代中叶，这里保留有大批宋元时代的瓷器碎片和窑膛、古井和作坊等遗址，现已列为国家重点文物保护单位。南市街古瓷窑遗址位于柳家湾以西的南市街，遗物上溯五代，下至元初。堆积层丰富，分布面广，产品器型精巧，釉面晶莹，多有精美的刻划花纹或印泥。瓷器品类繁多，有青白瓷、影青瓷、青花瓷等。此外，较著名的窑址还有塘下、杨梅亭和黄泥头等。
        </div>
         <div style="font-size: 15px; text-align: left; text-indent: 25px">
           一千多年来，景德镇是一座沿昌江兴建的没有城墙、对洪水不设防的城市，形成了长达6.5公里、宽约2公里的长条状布局。在1998年夏季长江流域遭遇特大洪水时，昌江也洪水泛滥，汛期市区两度被淹，最深处洪水达3米之深，损失惨重。洪水过后，景德镇投入6000万元巨资，修筑城市防洪工程。防洪工程在昌江支流南河段开掘，包括老鸦滩、西瓜洲等低洼地带圩堤的加固扩建，还包括防洪堤、排涝工程、疏浚河道和远期的吾溪口水利枢纽工程等，可使景德镇市的防洪能力由原来的5至7年一遇提高到20年至50年一遇。
        </div>
         <div style="font-size: 15px; text-align: left; text-indent: 25px">
           改革开放以来，景德镇的城市发展速度大大加快。昔日破旧矮小的陶舍，已作为古董陈列于历史博览区供游人参观，取而代之的是现代化的厂房。珠山大道、西郊大道、曙光大道、沿河路、中山路等与飞架昌江东西的珠山昌河大桥将整个市区联为一体，火车站、少年宫、电信大楼、百货大楼、景德镇饭店等风格各异的现代化建筑点缀其间，莲花塘、人民公园风景如画，人民广场气势壮观，千年古城已建设得雄伟壮丽。昌江两岸葱茏秀丽，城市生态环境大为改善。有着辉煌历史的景德镇人民，正快马加鞭，扬帆启航，为瓷都的发展再创佳绩！
